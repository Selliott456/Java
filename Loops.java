public class Loops {

  public static void main (String [] args){

    for (int i = 0; i < 100; i++){
      // do something
    }

    String [] myStrings = {"here", "is", "an", "array"};

    //  get the elements out of an array:
    for (String value: myStrings) {
      System.out.println(value);
    }

    int i = 0;
    while (i < 100){
      // do something
      i++;
    }

    
  }
  
}
